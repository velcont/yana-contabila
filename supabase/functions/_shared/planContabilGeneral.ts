export function validateAccountCode(code: string): { valid: boolean; name?: string; error?: string } {
  // Verifică dacă contul există exact în PCG
  if (PCG_ROMANIA_2025[code]) {
    return {
      valid: true,
      name: PCG_ROMANIA_2025[code]
    };
  }
  
  // Verifică prefixe (ex: 4111 derivă din 411, care derivă din 41)
  for (let i = code.length - 1; i >= 2; i--) {
    const prefix = code.substring(0, i);
    if (PCG_ROMANIA_2025[prefix]) {
      return {
        valid: true,
        name: `${PCG_ROMANIA_2025[prefix]} (subgrupă ${code})`
      };
    }
  }
  
  return {
    valid: false,
    error: `Contul ${code} NU EXISTĂ în Planul Contabil General România 2025`
  };
}

export function getAccountClass(code: string): number | null {
  const firstDigit = parseInt(code[0]);
  return (firstDigit >= 1 && firstDigit <= 8) ? firstDigit : null;
}
